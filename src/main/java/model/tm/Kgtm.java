package model.tm;

public class Kgtm {

    private int day;
    private int kg;

    public Kgtm(int day, int kg) {
        this.day = day;
        this.kg = kg;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getKg() {
        return kg;
    }

    public void setKg(int kg) {
        this.kg = kg;
    }
}
